import SwiftUI

public extension Color {

  // MARK: - Background Colors

  /// The primary background color used for most surfaces.
  static var backgroundPrimary: Color { Color(.backgroundPrimary) }

  /// The raised background color used for elevated surfaces such as cards or modals.
  static var backgroundRaised: Color { Color(.backgroundRaised) }

  /// The recessed background color used for elements like footers or secondary sections.
  static var backgroundRecessed: Color { Color(.backgroundRecessed) }

  // MARK: - Overlay Colors

  /// The primary foreground color used for standard text and icons.
  static var overlayDark: Color { Color(.overlayDark) }

  /// The primary foreground color used for standard text and icons.
  static var overlayLight: Color { Color(.overlayLight) }
  
  // MARK: - Primary Colors

  /// The primary blue color used for action buttons, links, and other primary interactive elements.
  static var bluePrimary: Color { Color(.bluePrimary) }

  /// The primary green color used for success messages, positive indicators, and confirmation elements.
  static var greenPrimary: Color { Color(.greenPrimary) }

  /// The primary green color used for success messages, positive indicators, and confirmation elements.
  static var midnightPrimary: Color { Color(.midnightPrimary) }

  /// The primary green color used for success messages, positive indicators, and confirmation elements.
  static var orangePrimary: Color { Color(.orangePrimary) }

  /// The primary green color used for success messages, positive indicators, and confirmation elements.
  static var purplePrimary: Color { Color(.purplePrimary) }

  /// The primary green color used for success messages, positive indicators, and confirmation elements.
  static var redPrimary: Color { Color(.redPrimary) }

  /// The primary green color used for success messages, positive indicators, and confirmation elements.
  static var yellowPrimary: Color { Color(.yellowPrimary) }

  // MARK: - Shadow Colors

  /// The shadow color used for drop shadows on elevated elements.
  static var shadow: Color { Color(.shadow) }
}
